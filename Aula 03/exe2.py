num1 = 10
num2 = 5

def Soma():
    return num1 + num2
def Subtracao():
    return num1 - num2
def Multiplicacao():    
    return num1 * num2
def Divisao():
    return num1 / num2

print("Soma: ", Soma())
print("Subtração: ", Subtracao())
print("Multiplicação: ", Multiplicacao())
print("Divisão: ", Divisao())